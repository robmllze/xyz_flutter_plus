class WCircle extends StatelessWidget {
  //
  //
  //

  final double? diameter;
  final Color? color;
  final Widget? child;

  //
  //
  //

  const WCircle({
    super.key,
    this.diameter,
    this.color,
    this.child,
  });

  //
  //
  //

  @override
  Widget build(BuildContext context) {
    return Container(
      width: this.diameter,
      height: this.diameter,
      decoration: const BoxDecoration(shape: BoxShape.circle),
      clipBehavior: Clip.antiAlias,
      child: this.child,
    );
  }
}
